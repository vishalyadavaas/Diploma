class all_loop {
    public static void main(String[] args) {
        
        // for loop
        System.out.println("For loop");
        for(int i=1; i<3; i++){
            System.out.println(i);
        }

        // while loop
        System.out.println("While loop");
        int j=1;
        while(j<3){
            System.out.println(j);
            j++;
        }

        // do while loop
        System.out.println("Do while loop");
        int k=1;
        do{
            System.out.println(k);
            k++;
        }while(k<3);
    }
}
