class Assignment_operators {
    public static void main(String[] args) {
        int x = 10;
        x+=5;
        System.out.println(x);
        //
        int a=10;
        a-=5;
        System.out.println(a);
        //
        int b=10;
        b*=5;
        System.out.println(b);
        //
        int c=10;
        c/=2;
        System.out.println(c);
        //
        int d=11;
        d%=2;
        System.out.println(d);
    }
}
