class Arithmetic_operators {
    public static void main(String[] args) {
        int x= 11;
        int y = 10;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);
        System.out.println(++x);
        System.out.println(--x);
    }
}
