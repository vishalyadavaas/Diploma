class datatype {
    public static void main(String[] args) {
        // Primitive Data Types Examples
        byte num1 = 10;
        short num2 = 20;
        int num3 = 30;
        long num4 = 40L;
        float num5 = 50.5f;
        double num6 = 60.6;
        boolean flag = true;
        char letter = 'A';

        System.out.println("Byte value: " + num1);
        System.out.println("Short value: " + num2);
        System.out.println("Integer value: " + num3);
        System.out.println("Long value: " + num4);
        System.out.println("Float value: " + num5);
        System.out.println("Double value: " + num6);
        System.out.println("Boolean value: " + flag);
        System.out.println("Character value: " + letter);

        // Reference Data Types Example
        String name = "vishal";
        int[] nums = { 1, 2, 3, 4, 5 };

        System.out.println("String value: " + name);
        System.out.println("Array value: " + nums[2]);
    }
}
